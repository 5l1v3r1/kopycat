class Not(core: x86Core, opcode: ByteArray, prefs: Prefixes, vararg operands: AOperand<x86Core>):
        AX86Instruction(core, Type.VOID, opcode, prefs, *operands) {
    override val mnem = "not"

    override fun execute() {
        val tmp = op1.value(core).inv()
        op1.value(core, tmp)
    }
}